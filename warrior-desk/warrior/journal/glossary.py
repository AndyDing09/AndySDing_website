"""The metrics glossary (Section 9). Written into the journal header so the
journal teaches, not just records."""

GLOSSARY = """\
## Glossary (the journal teaches — refer back here)

- **Float** — shares actually available to trade; lower float = more explosive moves.
- **RVOL (Relative Volume)** — today's volume vs the average for this time of day; >=2x = unusual interest.
- **VWAP** — volume-weighted average price; intraday "fair value"; holding above it is bullish.
- **9 / 20 / 200-EMA** — exponential moving averages; first / next / long-term support on pullbacks.
- **MACD** — trend/momentum oscillator; shows shifts in trend.
- **RSI** — overbought/oversold gauge; high RSI = extended, due for a pullback.
- **ATR** — average true range; how much the stock typically moves (volatility sanity check).
- **R / R-multiple** — risk unit (entry - stop). A "+2R" trade made twice what it risked.
- **Reward:Risk (R:R)** — projected reward / risk; the agent's hard floor is 2.0.
- **Win rate** — % of trades that are winners.
- **Profit factor** — gross profit / gross loss (>1 is profitable).
- **Expectancy** — average $ (or R) expected per trade = (win% x avg win) - (loss% x avg loss).
- **Spread** — bid-ask gap; wide spreads make small-caps expensive to enter/exit.
- **HOD** — high of day.
- **SSR / LULD / PDT** — short-sale restriction / limit-up-limit-down halt / pattern-day-trader rule.
"""
